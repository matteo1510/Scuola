package com.mycompany.primo_progetto_java;

public class Primo_Progetto_java {

    public static void main(String[] args) {
        int a=8;
        int b=2;
        int somma, differenza, prodotto;
        float rapporto;
        
        somma= a + b;
        differenza= a - b;
        prodotto= a * b;
        rapporto= a / b;
        
        System.out.println("Somma="+somma);
        System.out.println("Diferenza="+differenza);
        System.out.println("Prodotto="+prodotto);
        System.out.println("Rapporto="+rapporto);
    }
}
